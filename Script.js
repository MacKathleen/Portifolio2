function abrirProjeto1() {
    var projeto1 = document.getElementById("projeto1");
    var projeto2 = document.getElementById("projeto2");

    projeto1.style.display = "block";
    projeto2.style.display = "none";
}

function abrirProjeto2() {
    var projeto1 = document.getElementById("projeto1");
    var projeto2 = document.getElementById("projeto2");

    projeto1.style.display = "none";
    projeto2.style.display = "block";
}
